import { gql } from 'graphql-request';
import { IssuedQueryParams } from '../../src/types';
import { createGQLWhereString } from '../../src/utils';

export const createIssuedQuery = ({
	account,
	minBlock,
	autoGeneratedPaginationField,
}: IssuedQueryParams): string => {
	const whereString = createGQLWhereString(
		Object.entries({
			account: account != null ? 'account' : null,
			block_gte: minBlock != null ? 'minBlock' : null,
			timestamp_lt: autoGeneratedPaginationField != null ? 'autoGeneratedPaginationField' : null,
		})
	);

	return gql`
		query issueds($autoGeneratedPaginationField: Int, $minBlock: Int, $max: Int, $account: String) {
			issueds(
				first: $max
				where: ${whereString}
				orderBy: timestamp
				orderDirection: desc
			) {
        id
        account
        value
        source
        timestamp
        gasPrice
        block
			}
		}
	`;
};
