import { gql } from 'graphql-request';
import { RateUpdateQueryParams } from '../../src/types';
import { createGQLWhereString, createGQLBlockNumberString } from '../../src/utils';

export const createRateUpdatesQuery = (params?: RateUpdateQueryParams): string => {
	const whereString = createGQLWhereString(
		Object.entries({
			synth: (params?.synth ?? null) != null ? 'synth' : null,
			timestamp_gte: (params?.minTimestamp ?? null) != null ? 'minTimestamp' : null,
			timestamp_lt:
				(params?.autoGeneratedPaginationField ?? null) != null
					? 'autoGeneratedPaginationField'
					: null,
		})
	);

	return gql`
		query rateUpdates($autoGeneratedPaginationField: Int, $max: Int, $synth: String, $minTimestamp: Int) {
			rateUpdates(
				first: $max${createGQLBlockNumberString(params?.blockNumber ?? null)}
				where: ${whereString}
				orderBy: timestamp
				orderDirection: desc
			) {
				id
				currencyKey
				synth
				rate
				block
				timestamp
			}
		}
	`;
};
