import { gql } from 'graphql-request';
import { FeesClaimedParams } from '../../src/types';
import { createGQLWhereString, createGQLBlockNumberString } from '../../src/utils';

export const createFeesClaimedQuery = (params?: FeesClaimedParams): string => {
	const whereString = createGQLWhereString(
		Object.entries({
			account: (params?.account ?? null) != null ? 'account' : null,
			timestamp_lt:
				(params?.autoGeneratedPaginationField ?? null) != null
					? 'autoGeneratedPaginationField'
					: null,
		})
	);

	return gql`
		query feesClaimeds($autoGeneratedPaginationField: Int, $max: Int, $account: String) {
			feesClaimeds(
				first: $max${createGQLBlockNumberString(params?.blockNumber ?? null)}
				where: ${whereString}
				orderBy: timestamp
				orderDirection: desc
			) {
        id
        account
        value
        rewards
        timestamp
        block
			}
		}
	`;
};
