import { gql } from 'graphql-request';
import { DailyIssuedQueryParams } from '../../src/types';
import { createGQLBlockNumberString } from '../../src/utils';

export const createDailyIssuedQuery = (params?: DailyIssuedQueryParams): string => {
	return gql`
		query dailyIssueds($autoGeneratedPaginationField: Int, $minTimestamp: Int, $max: Int) {
			dailyIssueds(
				first: $max${createGQLBlockNumberString(params?.blockNumber ?? null)}
				orderBy: id
				orderDirection: desc
			) {
        id
        value
		totalDebt
			}
		}
	`;
};
